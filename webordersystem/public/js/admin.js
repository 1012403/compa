function markError(id) {
	$('#'+id).addClass("error_field");
}

function removeError(id) {
	$('#'+id).removeClass("error_field");
}


function isNumber(num) {
	return $.isNumeric( num );
}

function isNotNumber(num) {
	return !isNumber(num);
}

function isEmpty(val) {
	if (val) {
	    return false;
	} else {
		return true;
	}
}

